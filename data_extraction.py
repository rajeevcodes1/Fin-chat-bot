import pandas as pd

def main():
    all_rows = []

    for company in COMPANIES:
        name, cik = company["name"], company["cik"]
        print(f"Fetching {name}...", end=" ", flush=True)

        try:
            facts = fetch_company_facts(cik)
            entity_name = facts.get("entityName", name)

            results = {
                metric: extract_annual_values(facts, tags)
                for metric, tags in METRICS.items()
            }

            # Collect recent fiscal year-end dates
            all_dates = set()
            for r in results.values():
                all_dates.update(r["data"].keys())
            recent_dates = sorted(all_dates, reverse=True)[:NUM_YEARS]

            # Build one row per year
            for date in recent_dates:
                row = {"Company": entity_name, "Fiscal Year End": date, "Year": int(date[:4])}
                for metric, result in results.items():
                    val = result["data"].get(date)
                    row[metric] = val / 1_000_000_000 if val is not None else None  # convert to $B
                all_rows.append(row)

            print("done.")

        except Exception as e:
            print(f"ERROR — {e}")

    # Build DataFrame
    df = pd.DataFrame(all_rows)
    df = df.sort_values(["Company", "Year"], ascending=[True, False]).reset_index(drop=True)

    # Format dollar columns to 2 decimal places (in $B)
    metric_cols = list(METRICS.keys())
    df[metric_cols] = df[metric_cols].round(2)

    return df


df = main()

