# ── Helpers ────────────────────────────────────────────────────────────────────

def fetch_company_facts(cik: str) -> dict:
    url = f"https://data.sec.gov/api/xbrl/companyfacts/CIK{cik}.json"
    response = requests.get(url, headers=HEADERS, timeout=15)
    response.raise_for_status()
    return response.json()


def extract_annual_values(facts: dict, concept_tags: list) -> dict:
    """Try each fallback tag and return deduplicated annual (10-K) data points."""
    us_gaap = facts.get("facts", {}).get("us-gaap", {})

    for tag in concept_tags:
        if tag not in us_gaap:
            continue
        usd_data = us_gaap[tag].get("units", {}).get("USD", [])

        # Keep only annual 10-K filings
        annual = [
            e for e in usd_data
            if e.get("form") == "10-K" and e.get("fp") == FISCAL_YEAR
        ]
        if not annual:
            continue

        # Deduplicate: keep the latest-filed entry per fiscal year-end date
        by_year = {}
        for e in annual:
            end, filed = e["end"], e.get("filed", "")
            if end not in by_year or filed > by_year[end]["filed"]:
                by_year[end] = e

        return {
            "tag_used": tag,
            "data": {k: v["val"] for k, v in sorted(by_year.items(), reverse=True)},
        }

    return {"tag_used": None, "data": {}}


def format_value(val) -> str:
    """Format a dollar value in billions, or return 'N/A'."""
    if val is None:
        return "N/A"
    return f"${val / 1_000_000_000:,.2f}B"


def print_company_table(entity_name: str, results: dict):
    """Print a formatted table for one company."""
    all_dates = set()
    for r in results.values():
        all_dates.update(r["data"].keys())
    recent_dates = sorted(all_dates, reverse=True)[:NUM_YEARS]

    col_w = 14
    divider = "=" * (32 + col_w * len(recent_dates))

    print(f"\n{divider}")
    print(f"  {entity_name}")
    print(divider)

    header = f"{'Metric':<32}" + "".join(d[:4].rjust(col_w) for d in recent_dates)
    print(header)
    print("-" * len(header))

    for metric_name, result in results.items():
        row = f"{metric_name:<32}"
        for date in recent_dates:
            row += format_value(result["data"].get(date)).rjust(col_w)
        print(row)

    print(divider)


